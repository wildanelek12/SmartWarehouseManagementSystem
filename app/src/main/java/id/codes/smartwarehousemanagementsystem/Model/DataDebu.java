package id.codes.smartwarehousemanagementsystem.Model;

public class DataDebu {
    int debu;
    String waktu;

    public DataDebu(){}
    public DataDebu(int debu, String waktu) {
        this.debu = debu;
        this.waktu = waktu;
    }



    public int getDebu() {
        return debu;
    }

    public String getWaktu() {
        return waktu;
    }
}
